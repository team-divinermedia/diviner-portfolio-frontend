const getLiveStatusLabel = () => {
  const now = new Date();
  const hour = now.getHours();

  if (hour >= 10 && hour < 20) {
    const options = ["Just uploaded", "Updated 12 mins ago", "Updated 5 mins ago"];
    return options[now.getMinutes() % options.length];
  }

  const lastUpdate = new Date();
  lastUpdate.setHours(19, 55, 0, 0);
  const timeStr = lastUpdate.toLocaleTimeString([], {
    hour: "numeric",
    minute: "2-digit",
  });

  return `Last updated today at ${timeStr}`;
};

const formatRelativeTime = (isoString) => {
  const now = new Date();
  const then = new Date(isoString);
  const diffMs = now - then;
  const diffMins = Math.round(diffMs / 60000);

  if (diffMins < 60) return `${diffMins}m ago`;
  const diffHours = Math.round(diffMins / 60);
  if (diffHours < 24) return `${diffHours}h ago`;
  const diffDays = Math.round(diffHours / 24);
  return `${diffDays}d ago`;
};

const getISTDate = () => {
  const now = new Date();
  const istOffsetMinutes = 330; // UTC+5:30
  const utcMillis = now.getTime() + now.getTimezoneOffset() * 60000;
  return new Date(utcMillis + istOffsetMinutes * 60000);
};

const isActiveHour = () => {
  const hourIST = getISTDate().getHours();
  return hourIST >= 10 && hourIST < 20;
};

